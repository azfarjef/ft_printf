#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	size_t			i;
	unsigned char	*a1;
	unsigned char	*a2;

	i = 0;
	a1 = (unsigned char *)s1;
	a2 = (unsigned char *)s2;
	while (s1[i] && s2[i] && (s1[i] == s2[i]))
		i++;
	return (a1[i] - a2[i]);
}
